// Verifies the workspace member `node_modules` directories created by
// `deno install`.

function assertDir(path: string) {
  const stat = Deno.statSync(path);
  if (!stat.isDirectory) {
    throw new Error(`expected a directory at ${path}`);
  }
}

// A member's own npm dependency is materialized in its local `node_modules`
// (https://github.com/denoland/deno/issues/26743).
assertDir("package1/node_modules/@denotest/esm-basic");

// A member's devDependency is materialized in its local `node_modules` too.
assertDir("package2/node_modules/@denotest/cjs-default-export");

// A sibling workspace member is symlinked into the member's `node_modules`
// (https://github.com/denoland/deno/issues/27550).
assertDir("package2/node_modules/package1");

// and the link actually points at the sibling member.
const pkg = JSON.parse(
  Deno.readTextFileSync("package2/node_modules/package1/package.json"),
);
if (pkg.name !== "package1") {
  throw new Error(`expected package1, got ${pkg.name}`);
}

console.log("ok");
