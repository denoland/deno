// Verifies that after dropping a member's dependencies and reinstalling, the
// stale links are pruned from the member `node_modules` so the dropped
// dependencies are no longer resolvable from the member.

function assertMissing(path: string) {
  let exists = true;
  try {
    // lstat (not stat) so a dangling symlink still counts as present.
    Deno.lstatSync(path);
  } catch (err) {
    if (err instanceof Deno.errors.NotFound) {
      exists = false;
    } else {
      throw err;
    }
  }
  if (exists) {
    throw new Error(`expected ${path} to be pruned`);
  }
}

// The dropped npm dependency and its now-empty scope directory are gone.
assertMissing("package1/node_modules/@denotest/esm-basic");
assertMissing("package1/node_modules/@denotest");

// The dropped sibling workspace member link is gone.
assertMissing("package2/node_modules/package1");

// but a dependency that is still declared (here package2's devDependency) is
// kept, so pruning only removes the links that became stale.
const stat = Deno.statSync(
  "package2/node_modules/@denotest/cjs-default-export",
);
if (!stat.isDirectory) {
  throw new Error("expected package2's devDependency to be kept");
}

console.log("pruned");
