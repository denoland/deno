use deno_config::deno_json::NodeModulesDirMode;
use deno_core::error::AnyError;
use jsonc_parser::cst::CstInputValue;

use super::ConfigFix;
use super::DoctorCheck;
use super::DoctorContext;
use super::Finding;
use super::FindingSeverity;

/// The default `nodeModulesDir` mode in Deno 3 when the key is absent.
const DENO_3_DEFAULT_MODE: NodeModulesDirMode = NodeModulesDirMode::Manual;

/// Reports when the project has no explicit `nodeModulesDir` and the mode
/// Deno's heuristics currently pick differs from the Deno 3 default.
/// `--fix` pins the mode Deno picks today, so behavior is identical on
/// both versions.
pub struct NodeModulesDirCheck;

impl DoctorCheck for NodeModulesDirCheck {
  fn name(&self) -> &'static str {
    "node-modules-dir"
  }

  fn run(&self, ctx: &DoctorContext) -> Result<Vec<Finding>, AnyError> {
    // `nodeModulesDir` is a root-only option.
    let Some(root_deno_json) = ctx.workspace.root_deno_json() else {
      // Without a root deno.json there is no file to pin the mode in. A
      // package.json-only project resolves to "manual" already, which
      // matches the Deno 3 default, so there is nothing to report.
      return Ok(Vec::new());
    };
    if root_deno_json.json.node_modules_dir.is_some() {
      // Explicitly configured (the deprecated boolean form is handled by
      // the deprecated config check).
      return Ok(Vec::new());
    }

    let implicit_mode = ctx.node_modules_dir_mode;
    if implicit_mode == DENO_3_DEFAULT_MODE {
      return Ok(Vec::new());
    }

    let file = ctx.config_file_path(&root_deno_json.specifier)?;
    let file_display = ctx.display_path(&file);
    // Mirrors the heuristics in `WorkspaceFactory::raw_node_modules_dir_mode`
    // for the message only; the mode itself comes from that function.
    let reason = if ctx.workspace.root_pkg_json().is_some() {
      // Only reachable when the mode is not "manual" despite a
      // package.json, i.e. the package.json is inside the Deno cache
      // directory.
      "the workspace's package.json is inside the Deno cache directory"
        .to_string()
    } else if implicit_mode == NodeModulesDirMode::Auto {
      format!(
        "\"vendor\" is enabled in {file_display} and there is no package.json in the workspace root"
      )
    } else {
      "there is no package.json in the workspace root".to_string()
    };

    Ok(vec![Finding {
      id: "node-modules-dir-not-pinned",
      severity: FindingSeverity::Warning,
      message: format!(
        "\"nodeModulesDir\" is not set in {file_display}. Deno currently uses the \"{implicit}\" mode for this project (implicit because {reason}). Deno 3 changes the default to \"{deno3}\", which changes how npm packages are set up and resolved.",
        implicit = implicit_mode.as_str(),
        deno3 = DENO_3_DEFAULT_MODE.as_str(),
      ),
      file,
      fix_description: Some(format!(
        "set \"nodeModulesDir\": \"{}\" (pins the mode Deno currently picks because {})",
        implicit_mode.as_str(),
        reason,
      )),
      fix: Some(ConfigFix::SetRootKey {
        key: "nodeModulesDir",
        value: CstInputValue::String(implicit_mode.as_str().to_string()),
      }),
    }])
  }
}
