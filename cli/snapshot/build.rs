#[cfg(not(feature = "disable"))]
mod shared;

fn main() {
  // Trigger a snapshot rebuild whenever the import-graph instrumentation is
  // toggled (see libs/core/modules/import_graph.rs).
  #[allow(clippy::print_stdout, reason = "build script output")]
  {
    println!("cargo:rerun-if-env-changed=DENO_SNAPSHOT_IMPORT_GRAPH");
  }
  #[cfg(not(feature = "disable"))]
  {
    let o = std::path::PathBuf::from(std::env::var_os("OUT_DIR").unwrap());
    let cli_snapshot_path = o.join("CLI_SNAPSHOT.bin");
    let residual_path = o.join("EXTENSION_RESIDUAL_SOURCES.rs");
    create_cli_snapshot(cli_snapshot_path, residual_path);
  }
}

#[cfg(not(feature = "disable"))]
fn create_cli_snapshot(
  snapshot_path: std::path::PathBuf,
  residual_path: std::path::PathBuf,
) {
  use std::collections::HashSet;
  use std::io::Write;

  use deno_runtime::ops::bootstrap::SnapshotOptions;
  use deno_runtime::snapshot::LazyExtensionFileKind;

  let snapshot_options = SnapshotOptions {
    ts_version: shared::TS_VERSION.to_string(),
    v8_version: deno_runtime::deno_core::v8::VERSION_STRING,
    target: std::env::var("TARGET").unwrap(),
  };

  let output = deno_runtime::snapshot::create_runtime_snapshot(
    snapshot_path,
    snapshot_options,
    vec![],
  );

  let consumed: HashSet<&str> = output
    .consumed_lazy_specifiers
    .iter()
    .map(String::as_str)
    .collect();

  let out_dir = std::path::PathBuf::from(std::env::var_os("OUT_DIR").unwrap());

  let mut residual_js: Vec<(&str, String)> = Vec::new();
  let mut residual_esm: Vec<(&str, String)> = Vec::new();
  for file in &output.lazy_extension_files {
    if consumed.contains(file.specifier.as_str()) {
      continue;
    }
    // Make sure we rebuild when residual sources change.
    #[allow(clippy::print_stdout, reason = "build script output")]
    {
      println!("cargo:rerun-if-changed={}", file.path.display());
    }
    // Both lazy_loaded_js (loadExtScript -> v8::Script::compile on raw
    // source) and lazy_loaded_esm (op_lazy_load_esm -> LazyEsmModuleLoader,
    // which returns the source unchanged) skip transpilation at runtime.
    // The snapshot path applies extension_transpiler for consumed files;
    // for residuals we have to pre-transpile here so the runtime gets
    // parseable JS rather than TypeScript syntax.
    let transpiled = transpile_residual_source(&file.specifier, &file.path);
    let entry = (file.specifier.as_str(), transpiled);
    match file.kind {
      LazyExtensionFileKind::Js => residual_js.push(entry),
      LazyExtensionFileKind::Esm => residual_esm.push(entry),
    }
  }

  // Pack every residual source into one blob written to OUT_DIR. Release
  // binaries get this blob appended as a separate Mach-O segment / ELF
  // trailer / PE resource by the `sui` post-link step (see tools/sui_pack
  // or invoke libsui::Macho::write_section directly with name "dnclbk"),
  // so the kernel can leave those pages demand-paged at idle. Dev builds
  // (no post-link) fall back to an `include_bytes!` of the same blob; the
  // bytes are then resident but `cargo run` works without ceremony.
  let blob_path = out_dir.join("RESIDUAL_BLOB.bin");
  let mut blob = Vec::<u8>::new();
  let js_layout = pack_into_blob(&mut blob, &residual_js);
  let esm_layout = pack_into_blob(&mut blob, &residual_esm);
  std::fs::write(&blob_path, &blob).unwrap();

  let mut f = std::fs::File::create(&residual_path).unwrap();
  writeln!(
    f,
    "// Copyright 2018-2026 the Deno authors. MIT license.\n\
     // @generated by cli/snapshot/build.rs - do not edit.\n"
  )
  .unwrap();
  write_runtime_preamble(&mut f);
  write_manifest(&mut f, "RESIDUAL_LAZY_JS", &js_layout);
  write_manifest(&mut f, "RESIDUAL_LAZY_ESM", &esm_layout);
}

#[cfg(not(feature = "disable"))]
fn pack_into_blob<'a>(
  blob: &mut Vec<u8>,
  entries: &[(&'a str, String)],
) -> Vec<(&'a str, u32, u32)> {
  let mut out = Vec::with_capacity(entries.len());
  for (specifier, source) in entries {
    let bytes = source.as_bytes();
    let offset = u32::try_from(blob.len()).expect("residual blob >4GiB");
    let len = u32::try_from(bytes.len()).expect("residual source >4GiB");
    blob.extend_from_slice(bytes);
    out.push((*specifier, offset, len));
  }
  out
}

#[cfg(not(feature = "disable"))]
fn write_runtime_preamble(f: &mut std::fs::File) {
  use std::io::Write;
  writeln!(
    f,
    r#"// Blob contains every residual lazy source concatenated. Specifiers
// resolve via (offset, len) into it.
//
// With `residual_sidecar` ON: the runtime requires a libsui section named
// "dnclbk" — its own LC_SEGMENT_64 / ELF trailer / PE resource that the
// kernel keeps demand-paged. Missing section -> startup panic.
//
// With `residual_sidecar` OFF (default, dev convenience): the same blob
// is baked via `include_bytes!`; the bytes end up resident but `cargo
// run` works without ceremony. We deliberately do NOT consult libsui in
// this mode: on Intel macOS, `libsui::find_section` is name-blind (it
// searches the file backward for the libsui sentinel and returns whatever
// was appended last), so a `deno compile`-produced binary — which appends
// a `d3n0l4nd` section — would have our `find_section("dnclbk")` return
// the user's compiled JS instead of None, and the residual table would
// index out of bounds.
#[cfg(not(feature = "residual_sidecar"))]
static FALLBACK_BLOB: &[u8] =
  include_bytes!(concat!(env!("OUT_DIR"), "/RESIDUAL_BLOB.bin"));

#[cfg(not(feature = "residual_sidecar"))]
fn residual_blob() -> &'static [u8] {{
  FALLBACK_BLOB
}}

#[cfg(feature = "residual_sidecar")]
fn residual_blob() -> &'static [u8] {{
  static BLOB: std::sync::LazyLock<&'static [u8]> =
    std::sync::LazyLock::new(|| match libsui::find_section("dnclbk") {{
      Ok(Some(bytes)) => bytes,
      other => panic!(
        "residual_sidecar feature requires a libsui 'dnclbk' section in the binary; \
         run `sui dnclbk <bin> RESIDUAL_BLOB.bin <out>` after linking (got {{other:?}})",
      ),
    }});
  *BLOB
}}

fn build_table(
  manifest: &'static [(&'static str, u32, u32)],
) -> Vec<(&'static str, &'static str)> {{
  let blob = residual_blob();
  manifest
    .iter()
    .map(|(specifier, offset, len)| {{
      let start = *offset as usize;
      let end = start + *len as usize;
      // SAFETY: build.rs sourced the bytes from a Rust `String` (transpiled
      // JS, valid UTF-8) and copied them unchanged into the blob.
      let code =
        unsafe {{ core::str::from_utf8_unchecked(&blob[start..end]) }};
      (*specifier, code)
    }})
    .collect()
}}

pub fn residual_lazy_js() -> &'static [(&'static str, &'static str)] {{
  static V: std::sync::LazyLock<Vec<(&'static str, &'static str)>> =
    std::sync::LazyLock::new(|| build_table(&RESIDUAL_LAZY_JS));
  &V
}}

pub fn residual_lazy_esm() -> &'static [(&'static str, &'static str)] {{
  static V: std::sync::LazyLock<Vec<(&'static str, &'static str)>> =
    std::sync::LazyLock::new(|| build_table(&RESIDUAL_LAZY_ESM));
  &V
}}
"#
  )
  .unwrap();
}

#[cfg(not(feature = "disable"))]
fn transpile_residual_source(
  specifier: &str,
  src_path: &std::path::Path,
) -> String {
  use deno_runtime::deno_core::ModuleCodeString;
  use deno_runtime::deno_core::ModuleName;
  use deno_runtime::transpile::maybe_transpile_source;

  let source = std::fs::read_to_string(src_path).unwrap_or_else(|e| {
    panic!(
      "failed to read residual lazy source {}: {e}",
      src_path.display()
    )
  });
  let (transpiled, _source_map) = maybe_transpile_source(
    ModuleName::from(specifier.to_string()),
    ModuleCodeString::from(source),
  )
  .unwrap_or_else(|e| {
    panic!("failed to transpile residual lazy source {specifier}: {e}")
  });
  transpiled.to_string()
}

#[cfg(not(feature = "disable"))]
fn write_manifest(
  f: &mut std::fs::File,
  name: &str,
  entries: &[(&str, u32, u32)],
) {
  use std::io::Write;
  writeln!(f, "const {name}: [(&str, u32, u32); {}] = [", entries.len())
    .unwrap();
  for (specifier, offset, len) in entries {
    writeln!(f, "  ({specifier:?}, {offset}, {len}),").unwrap();
  }
  writeln!(f, "];\n").unwrap();
}
